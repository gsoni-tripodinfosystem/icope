            <p >Frontline health workers (FLHWs), also known as community health workers or basic health
workers, are those who take health services directly to the grassroots level in communities
where access to health services is often limited. They are the key players in implementing
universal health coverage. They are the peripheral health workers of the government, and
provide services in clinic and outreach settings in Member States of the WHO South-East Asia
(SEA) Region.</p>
<p >
Demographic ageing has posed various unanticipated challenges to health-care systems and
services. With ageing, the physical and mental capacities (intrinsic capacities) of an individual
decline. Trained FLHWs can detect such declines in intrinsic capacity, and deliver eff ective
interventions to prevent and delay progression, if they are provided with the required knowledge
and skills. FLHWs can not only act as an essential link to health facilities by bringing services to
older peopleâ€™s doorsteps, but also be a lifeline for them.</p>
<p >
By virtue of this training, FLHWs are expected to be competent to handle various
responsibilities. These include conducting regular health surveys of families (including older
people), maintaining files and records, spreading awareness on health and nutrition, screening
noncommunicable diseases (NCDs) in older people, assessing their intrinsic capacities at their
doorsteps and, integrating services between homes and health service or wellness centres.
FLHWs should also be able to guide and facilitate the process of temporary rehabilitation and
the early discovery of functional impairment.</p>
<p >
The training is intended to teach FLHWs to improve their skills in screening, assessing and
managing various physical, psychological as well as social aspects of old-age care in the
community, as well as in the outpatient department (OPD) settings.</p>
